use abacus_lib;

pub fn main() {
    println!("Abacus");
    let key:[u8; 16] = [0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08];
    let data:[u8; 16] = [0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08];
    println!("data={:?}", data);
    let sm4 = abacus_lib::algorithm::symmetric_encryption::sm4::sm4_encryption::SM4Encryption::with_key(key);
    let e = sm4.encrypt(&data);
    println!("e={:?}", e);
    let de = sm4.decrypt(&e);
    println!("de={:?}", de);
}
